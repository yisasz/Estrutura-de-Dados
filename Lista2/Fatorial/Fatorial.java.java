package executavel;

import java.util.Scanner;

public class Fatorial {

	public static int fat(int n) {

		if (n == 0 || n == 1) {
			return 1;
		}

		return n * fat(n - 1);
	}

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);

		int n = scanner.nextInt();

		System.out.println(fat(n));

		scanner.close();
	}
}
